package com.hillel.borysov.examples.constructors;

public class Crocodile {
    String name;
    int age;
    String clas;
    String habitat;
    double weight;
    double lenght;

    public Crocodile(String myName, int myAge, String myclas, String myHabitat, double myWeight, double myLenght) {
        name = myName;
        age = myAge;
        clas = myclas;
        habitat = myHabitat;
        weight = myWeight;
        lenght = myLenght;

    }


}
